 class Sampler {
public int height;
public int weight;
public int depth;

public static void main(String[] arguments) {
	Sampler thing = new Sampler();
	thing.height = 72;
	thing.weight = 1000;
	thing.depth  = 420;
    System.out.println("Height: " + thing.height);
	System.out.println("Weight " + thing.weight);
	System.out.println("Depth: " + thing.depth);
	}
 }
 
	
	
	